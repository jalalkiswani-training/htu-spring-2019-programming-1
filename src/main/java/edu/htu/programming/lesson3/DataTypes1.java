package edu.htu.programming.lesson3;

public class DataTypes1 {
	public static void main(String[] args) {
		//int: data type
		//i: variable name
		//10: initial value
		int i1=10;
		int i2=20;
		
		//+ : operator , - , * , / , % 
		int sum=i1+i2;
		
		//"XYZ" any thing between double quotation is called string (a sequence of charcaters)
		//String + anything else, will cause a String concatination 
		System.out.println("The value of i is :"+i1);
		System.out.println("The sum is : "+sum);
		
		
		//byte: data type
		//b: variable name
		//100: initial value
		byte b=100;
		
	}
}
